export function getHistoryEntry(pinId, tier = "kid") {
  const entry = HISTORY_CONTENT[pinId];
  if (!entry) return null;

  if (tier === "adult") return entry.adult || entry.teen || entry.kids || null;
  if (tier === "teen") return entry.teen || entry.kids || entry.adult || null;
  return entry.kids || entry.teen || entry.adult || null;
}

export function hasHistoryEntry(pinId) {
  return !!HISTORY_CONTENT[pinId];
}
